        }

        [Fact]
        public void TestResultCalculation_Draw()
        {
            // *************************************************************************************************
            // Arrange
            // *************************************************************************************************

            var generator = new DJAmlin.Modules.RockGenerator();

            // *************************************************************************************************
            // Act
            // *************************************************************************************************

            var res = generator.CalculateWinner(Definitions.Rock, Definitions.Rock);

            // *************************************************************************************************
            // Assert
            // *************************************************************************************************

            res.Should().Be(Winner.Draw);
        }

    }
