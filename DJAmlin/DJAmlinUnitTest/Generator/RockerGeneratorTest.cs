using DJAmlin.Controllers;
using DJAmlin.Models;
using DJAmlinUnitTest.Builder;
using FluentAssertions;
using System;
using System.Collections.Generic;
using System.Text;
using Xunit;

namespace DJAmlinUnitTest.Generator
{
   public  class RockerGeneratorTest
    {
        [Fact]
        public void TestResultCalculation_1()
        {
            // *************************************************************************************************
            // Arrange
            // *************************************************************************************************

            var generator = new DJAmlin.Modules.RockGenerator();

            // *************************************************************************************************
            // Act
            // *************************************************************************************************

            var res = generator.CalculateWinner(Definitions.Rock, Definitions.Paper);

            // *************************************************************************************************
            // Assert
            // *************************************************************************************************

            res.Should().Be(Winner.Player2);
        }

        [Fact]
        public void TestResultCalculation_2()
